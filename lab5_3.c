#include <stdio.h>
int main()
{
    int n,count=1,num,re=1,an=0;
    scanf("%d",&n);
    while(count<n)
    {
        num=re++;
    while(num%2==0)
    {
        num%=2;
        if(num==0)
            break;
    }
    while(num%3==0)
    {
         num%=3;
         if(num==0)
            break;
    }
    while(num%5==0)
    {
        num%=5;
        if(num==0)
            break;
    }
    if(num%7==0)
        an++;
    if(num==0)
    {
        count++;
        an++;
    }
    if(num>=1)
        an++;
    }
    if(n==1)
        an=1;
    printf("%d",an);
}
